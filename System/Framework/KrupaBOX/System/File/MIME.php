    public static function getExtensionByMIME($stringMIME)
    {
        $stringMIME = stringEx($stringMIME)->toLower();
        
        foreach (self::$MIMEs as $_MIME)
            if ($_MIME["MIME"] == $stringMIME)
                return stringEx($_MIME["extension"])->toUpper();
                
        return null;
    }
    
    public static function getMIMEsByExtension($extension)
    {
        $extension = stringEx($extension)->toLower();
        
        $MIMEs = \Arr();
        
        foreach (self::$MIMEs as $MIME)
            if ($MIME["extension"] == $extension)
                $MIMEs[] = $MIME["MIME"];
        
        return $MIMEs;
    }

    public static function getMIMEByFilePath($filePath)
    {
        if (\File::isFile($filePath) == false || \File::isReadable($filePath) == false)
            return null;

        $mime = "";
        if (function_exists("mime_content_type"))
            $mime = stringEx(mime_content_type($filePath))->toString();
        if (stringEx($mime)->isEmpty()) return null;

        return $mime;
    }
    
